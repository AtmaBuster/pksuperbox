def get_exp_at_level(lv, growth_type):
	if lv == 1: return 0
	if growth_type == 0:
		# med fast
		return lv * lv * lv
	elif growth_type == 1:
		# erratic
		if lv < 50: return (lv * lv * lv * (100 - lv)) // 50
		if lv < 68: return (lv * lv * lv * (150 - lv)) // 100
		if lv < 98: return (lv * lv * lv * ((1911 - 10 * lv) // 3)) // 500
		return (lv * lv * lv * (160 - lv)) // 100
	elif growth_type == 2:
		# fluctuating
		if lv < 15: return (lv * lv * lv * ((lv + 1) // 3 + 24)) // 50
		if lv < 36: return (lv * lv * lv * (lv + 14)) // 50
		return (lv * lv * lv * ((lv // 2) + 32)) // 50
	elif growth_type == 3:
		# med slow
		return 6 * lv * lv * lv // 5 - 15 * lv * lv + 100 * lv - 140
	elif growth_type == 4:
		# fast
		return (4 * lv * lv * lv) // 5
	elif growth_type == 5:
		# slow
		return (5 * lv * lv * lv) // 4
	else:
		raise Exception

def get_mon_level(exp, growth_type):
	for i in range(1, 101):
		if exp < get_exp_at_level(i, growth_type):
			return i - 1
	return 100

