class Hist:
	def __init__(self):
		self.d = {}
		self.tot = 0
	def __setitem__(self, ind, val):
		# discard val, just increment @ ind
		if not ind in self.d.keys():
			self.d[ind] = 0
		self.d[ind] += 1
		self.tot += 1
	def __getitem__(self, ind):
		return self.d[ind]

	def get_pcnt(self, t):
		k = int(self.tot * t)
		a = 0
		for key in sorted(self.d.keys()):
			amt = self.d[key]
			a += amt
			if a > k:
				return key
		return max(self.d.keys())

