def get_dex_number(game, ind):
	shr = pokedb.is_shared_ind(ind)
	if shr and shr != ind:
		return get_dex_number(game, shr)
	nat_dex = get_game_info(game, 'nat_dex')
	if not ind in nat_dex: return None
	return nat_dex.index(ind)

def get_is_in_regional_dex(game, ind):
	dex_num = get_dex_number(game, ind)
	if dex_num is None: return False
	r_dex = get_game_info(game, 'regional_bitmask')
	return bool((1 << (dex_num - 1)) & r_dex)

