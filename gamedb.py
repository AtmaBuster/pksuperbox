def get_game_info(game, key):
	game_dict = GAME_DATA[game]
	if key in game_dict.keys():
		return game_dict[key]
	return get_game_info(game_dict['base_game'], key)

def get_mon_index(game, ind):
	ind_map = get_game_info(game, 'index_map')
	if isinstance(ind_map, dict):
		return ind_map.get(ind, ind)
	return ind_map[ind]

def get_mon_index_r(game, ind):
	ind_map = get_game_info(game, 'index_map_r')
	if ind_map is None:
		return get_game_info(game, 'index_map_r').index(ind)
	return ind_map.get(ind, ind)

